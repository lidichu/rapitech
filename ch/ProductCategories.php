        var myImg = $('.list img').prop('src');
        var myTitle = $(this).parent().children()[1].innerText;//$('.list .product-title').text();
        var newProduct = "";
        var select = $('.cart-overview').children();
        for(i = 0;i<select.length;i++)
        {
            if(!select[i])
                break;

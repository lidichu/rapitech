           if(title== myTitle) 
                break;
        }
        if(title!= myTitle && title !="")
        {
            newProduct += "<li>";
            newProduct += "<a href=\"#\">";
            newProduct += "<img src=\"" + myImg + "\" class=\"img-responsive product-thumb  col-sm-6\">";
